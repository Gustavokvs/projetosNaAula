package exercicios;

public class Pessoas {

    public void exibirDados(String nome) {

        System.out.println("Nome: " + nome);

    }

    public void exibirDados(String nome, String idade) {

        System.out.println("\nNome: " + nome + "\nIdade: " + idade);

    }

    public void exibirDados(String nome, String idade, String cidade) {

        System.out.println("\nNome: " + nome + "\nIdade: " + idade + "\nCidade: " + cidade);

    }

}
