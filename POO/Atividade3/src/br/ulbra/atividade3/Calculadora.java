package br.ulbra.atividade3;

public class Calculadora {

    private double numero;
    

    public Calculadora() {
        this.numero = numero;

    }

    public double getNumero() {
        return numero;
    }

    public void setNumero(double numero) {
        this.numero = numero;
    }

    
    public double calcularRaiz(double numero) {

        return Math.sqrt(numero);

    }

    public double calcularPotencia(double numero, double numero2) {

        return Math.pow(numero, numero2);

    }
    
    
}
    
